function arrayPairsSum (nums) {
  let ordered = nums.sort((a,b) => {
    return a-b;
  });
  let i = 0;
  let finalSum = 0;
  while (i < ordered.length) {
      finalSum += ordered[i];
      i = i+2;
  }
  return finalSum;
}

